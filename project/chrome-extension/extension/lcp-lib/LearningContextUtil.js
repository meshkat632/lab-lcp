// Implementing a random number generator function like mt_rand in PHP
//**********************************************************************************************************************
//**********************************************************************************************************************
//**********************************************************************************************************************
function mt_rand($min,$max)
{
    return Math.floor(Math.random()*($max-$min+1)+$min);
}

